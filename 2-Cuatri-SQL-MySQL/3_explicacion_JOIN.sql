## Explicación JOINS
#
# INNER JOIN -> Devuelve el subconjunto que tienen coincidencia en 
#       ambas tablas. Sería:    A intersección B.
#
# LEFT JOIN -> Devuelve todas las filas de la tabla izquierda y
#       las filas coincidentes con la tabla derecha.
#      
# RIGHT JOIN -> Devuelve todas las filas de la tabla derecha y
#       las filas coincidentes con la tabla izquierda.
#
# FULL JOIN -> Devuelve cualquier coincidencia.
#       Sería:    A unión B.
#